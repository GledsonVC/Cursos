# Laço while
n = 10
cont = 0
while cont < n:
    print(cont)
    cont += 1

# Laço for
n = 10

cont = 0
for cont in range(n):
    print(cont)

# For declarando variavel nele indo de 2 até 6
for i in range(2, 6):
    print(i)

# For declarando variavel nele indo de 2 até 16 de 2 em 2
for i in range(2, 16, 2):
    print(i)

# For declarando variavel nele indo de 20 até 0 decrementando -2
for i in range(20, 0 ,-2):
    print(i)

